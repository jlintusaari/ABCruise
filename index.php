$(document).ready(function() {
  //prevent some jitter on mobile
  var cachedWidth = $(window).width();
  var splashHeight = $('div.splash-container').height();
  var wrapperOffset = $('div.content-wrapper').offset()

  $(window).resize(function(e){
    var newWidth = $(window).width();
    if(newWidth == cachedWidth){
      $('div.splash-container').height(splashHeight);
      $('div.content-wrapper').offset(wrapperOffset);
    }
    else {
      $('div.content-wrapper').removeAttr('style');
      cachedWidth = newWidth;
      wrapperOffset = $('div.content-wrapper').offset()
    }
  });
});

$(function() {
    $('a#gob').click(function(e) {
        e.preventDefault();
        var aTag = $('#acontent');
        $('html,body').animate({'scrollTop': aTag.offset().top}, 'slow');
    });
});
